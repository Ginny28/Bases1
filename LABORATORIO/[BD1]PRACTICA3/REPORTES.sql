SELECT        ARTISTA, REPRO,USERID
FROM            (SELECT DISTINCT ARTISTA, COUNT(NOMBRE) AS REPRO, USERID
                          FROM            (SELECT        CA.NOMBRE, CA.ARTISTA, RE.USERID, US.PAIS
                                                    FROM            CANCION CA, REPRODUCCION RE, USUARIO US
                                                    WHERE        CA.IDCANCION = RE.MUSICA AND RE.USERID = US.ID_USER) T1
                          GROUP BY ARTISTA, USERID) T2
WHERE        (REPRO >= 1) AND  USERID = 'USUARIO1'
ORDER BY REPRO

