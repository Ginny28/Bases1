SELECT  DISTINCT T1.PAIS   FROM (SELECT  US.PAIS,RE.MUSICA,CA.GENERO  FROM USUARIO US INNER JOIN REPRODUCCION RE  ON  US.ID_USER = RE.USERID 
INNER JOIN CANCION CA ON RE.MUSICA = CA.IDCANCION ) T1
WHERE  T1.GENERO = 'Pop' ;


