SELECT        PAIS, ARTISTA, REPRO
FROM            (SELECT DISTINCT PAIS, ARTISTA, COUNT(NOMBRE) AS REPRO
                          FROM            (SELECT        CA.NOMBRE, CA.ARTISTA, RE.USERID, US.PAIS
                                                    FROM            CANCION CA, REPRODUCCION RE, USUARIO US
                                                    WHERE        CA.IDCANCION = RE.MUSICA AND RE.USERID = US.ID_USER ) T1
                          GROUP BY PAIS, ARTISTA) T2
WHERE        (REPRO >= 1)  AND   ARTISTA = 'Ace of Base'
ORDER BY REPRO  




