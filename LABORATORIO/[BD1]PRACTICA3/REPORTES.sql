SELECT  LA.PAIS  FROM(
SELECT  DISTINCT T1.PAIS, COUNT(T1.ID_USER) AS REPRO   FROM
(SELECT  US.ID_USER, US.PAIS,RE.MUSICA  FROM USUARIO US INNER JOIN REPRODUCCION RE  ON  US.ID_USER = RE.USERID) T1
GROUP BY T1.PAIS ORDER BY REPRO DESC ) LA  WHERE ROWNUM <= 5 ; 


-------------- REPORT 2 -------------------------
SELECT  LA.GENERO  FROM(
SELECT DISTINCT T1.GENERO, COUNT(T1.GENERO) AS GENDER  FROM(
SELECT  SON.NOMBRE,SON.ARTISTA,SON.GENERO,RE.MUSICA  FROM CANCION SON INNER JOIN REPRODUCCION RE  ON  SON.IDCANCION = RE.MUSICA AND SON.GENERO IS NOT NULL ) T1
GROUP BY T1.GENERO   ORDER BY  GENDER DESC) LA WHERE ROWNUM<=5;

